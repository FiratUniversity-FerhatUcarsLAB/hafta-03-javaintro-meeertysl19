public class Dikdortgen {
    public static void main(String[] args) {
        double en = 4.5;
        double boy = 7.9;

        double alan = en * boy;
        double cevre = 2 * (en + boy);

        System.out.printf("Dikdörtgen En: %.2f, Boy: %.2f%n", en, boy);
        System.out.printf("Alan = %.4f%n", alan);
        System.out.printf("Çevre = %.4f%n", cevre);
    }
}
